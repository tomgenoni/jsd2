function subtract(x, y) {
  var value = x - y;
  return value;
}

function add(x, y) {
  var value = x + y;
  return value;
}

function multiply(x, y) {
  var value = x * y;
  return value;
}

function divide(x, y) {
  var value = x / y;
  return value;
}

function remainder(x, y) {
  var value = x % y;
  return value;
}
